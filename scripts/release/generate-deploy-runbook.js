#!/usr/bin/env node
/**
 * scripts/release/generate-deploy-runbook.js (ClaudeOS v8.2.4+)
 *
 * state.json の deploy.ready=true を検知して、deploy-runbook-template.md から
 * 実値を埋めた reports/deploy-runbook.md を生成する。
 *
 * **重要**: 実際のデプロイは人間が手動で実行する。本スクリプトは手順書を生成するだけ。
 *
 * 使い方:
 *   node scripts/release/generate-deploy-runbook.js
 *   node scripts/release/generate-deploy-runbook.js --env production
 *   node scripts/release/generate-deploy-runbook.js --force      # deploy.ready が false でも生成
 *   node scripts/release/generate-deploy-runbook.js --dry        # stdout のみ
 */

"use strict";

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const args = process.argv.slice(2);
const opts = { dry: false, force: false, env: "production" };
for (let i = 0; i < args.length; i++) {
  const a = args[i];
  if (a === "--dry") opts.dry = true;
  else if (a === "--force") opts.force = true;
  else if (a === "--env") opts.env = args[++i];
}

const cwd = process.cwd();
const STATE_FILE = path.join(cwd, "state.json");
const TEMPLATE_FILE = path.resolve(__dirname, "..", "..", "Claude", "templates", "claudeos", "docs", "deploy-runbook-template.md");
const OUT_FILE = path.join(cwd, "reports", "deploy-runbook.md");

function readJson(file) { try { return JSON.parse(fs.readFileSync(file, "utf8")); } catch { return null; } }
function safeGit(cmd) {
  try { return execSync(cmd, { cwd, encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] }).trim(); }
  catch { return ""; }
}

function buildContext(state) {
  const stable = state.stable || {};
  const project = state.project || {};
  const goal = state.goal || {};
  return {
    DATE: new Date().toISOString().slice(0, 10),
    PROJECT: goal.title || path.basename(cwd),
    ENVIRONMENT: opts.env,
    STABLE_STATUS: stable.stable_achieved
      ? `✅ STABLE 達成（連続 ${stable.consecutive_success} 回 / target ${stable.target_n}）`
      : `⚠️ STABLE 未達（連続 ${stable.consecutive_success || 0} 回）`,
    STABLE_PR: stable.stable_achieved_pr ? `#${stable.stable_achieved_pr}` : "—",
    GIT_BRANCH: safeGit("git rev-parse --abbrev-ref HEAD") || "?",
    GIT_HEAD: safeGit("git rev-parse --short HEAD") || "?",
    GIT_TAG: safeGit("git describe --tags --abbrev=0") || "(no tag)",
    RELEASE_DEADLINE: project.release_deadline || "(未設定)",
    DEPLOY_READY: state.deploy && state.deploy.ready ? "true" : "false",
    GENERATED_AT: new Date().toISOString(),
  };
}

function render(template, ctx) {
  return template.replace(/\{([A-Z_][A-Z0-9_]*)\}/g, (_, key) => (key in ctx ? ctx[key] : `{${key}}`));
}

function main() {
  if (!fs.existsSync(TEMPLATE_FILE)) {
    console.error(`[deploy-runbook] template not found: ${TEMPLATE_FILE}`);
    process.exit(2);
  }
  const state = readJson(STATE_FILE) || {};
  const deployReady = !!(state.deploy && state.deploy.ready);

  if (!deployReady && !opts.force) {
    console.log("[deploy-runbook] state.deploy.ready is not true. Skip generation.");
    console.log("                 Set state.deploy.ready=true, or pass --force to generate anyway.");
    process.exit(0);
  }

  const ctx = buildContext(state);
  const template = fs.readFileSync(TEMPLATE_FILE, "utf8");
  const header = `<!-- Auto-generated by scripts/release/generate-deploy-runbook.js at ${ctx.GENERATED_AT} -->
<!-- 重要: 実際のデプロイ実行は人間（ユーザー）が手動で行う。本書は手順書のみ。 -->

`;
  const rendered = header + render(template, ctx);

  if (opts.dry) {
    console.log(rendered);
    return;
  }

  fs.mkdirSync(path.join(cwd, "reports"), { recursive: true });
  fs.writeFileSync(OUT_FILE, rendered, "utf8");
  console.log(`[deploy-runbook] wrote ${path.relative(cwd, OUT_FILE)}`);
  console.log(`[deploy-runbook] env=${ctx.ENVIRONMENT} stable=${ctx.STABLE_STATUS}`);
  console.log("[deploy-runbook] ⚠️ デプロイ実行は人間が手動で行ってください");
}

if (require.main === module) main();

module.exports = { buildContext, render };
